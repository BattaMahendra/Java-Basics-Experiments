/*
WHAT ARE STREAMS ?

* Simply put, streams are wrappers around a data source, allowing us to operate with that data source and making bulk processing convenient and fast.
* A stream does not store data and, in that sense, is not a data structure.
* It also never modifies the underlying data source.


WHY STREAMS ?
     1.more readable and maintainable code.
     2.Functional Approach: Streams support functional programming paradigms by allowing operations like map, filter, reduce, etc.,
     which encourage immutability and statelessness,
     making your code less error-prone and easier to reason about.
     3. Lazy Evaluation: Streams perform operations on elements of a collection only when necessary.
      This laziness can lead to better performance by avoiding unnecessary computations.
     4. Lazy evaluation.

 */